public class RecruitUser {

    public String name;
    public String regNo;
    public String year;
    public String mobNo;
    public String interests;

    public RecruitUser() {

    }

    public RecruitUser(String name, String regNo, String year, String mobNo, String interests) {
        this.name = name;
        this.regNo = regNo;
        this.year = year;
        this.mobNo = mobNo;
        this.interests = interests;
    }

}
