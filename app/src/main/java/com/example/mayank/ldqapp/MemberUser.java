public class MemberUser {

    public String name;
    public String regNo;
    public String year;
    public String mobNo;

    public MemberUser() {
    }

    public MemberUser(String name, String regNo, String year, String mobNo) {
        this.name = name;
        this.regNo = regNo;
        this.year = year;
        this.mobNo = mobNo;
    }

}
